CREATE TABLE hardiness (
  uid INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  hardiness INT
);

CREATE TABLE species (
  uid INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  species VARCHAR(255)
);

CREATE TABLE materials (
  uid INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  material VARCHAR(255)
);

CREATE TABLE images(
  uid INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255)
);

CREATE TABLE species_materials (
  uid INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  species_id INT REFERENCES species(uid),
  material_id INT REFERENCES materials(uid),
  image_id INT REFERENCES images(uid),
  link VARCHAR(255)
);

CREATE TABLE species_hardiness (
  uid INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  species_id INT REFERENCES species(uid),
  hardiness_id INT REFERENCES hardiness(uid)
);
